    fun HideKeyboard(activity: Activity) {
        val imm: InputMethodManager =
            activity.getSystemService(Activity.INPUT_METHOD_SERVICE) as InputMethodManager
        //Find the currently focused view, so we can grab the correct window token from it.
        var view: View? = activity.currentFocus
        //If no view currently has focus, create a new one, just so we can grab a window token from it
        if (view == null) {
            view = View(activity)
        }
        imm.hideSoftInputFromWindow(view.windowToken, 0)
    }

    fun ShowSnackBar(
        ac: Activity,
        Message: String?,
        parentLayout: View?
    ): Snackbar {
        val mSnackBar = Snackbar.make(parentLayout!!, Message!!, Snackbar.LENGTH_LONG)
        val view = mSnackBar.view
        view.setPadding(5, 5, 5, 5)
        view.setBackgroundColor(ac.resources.getColor(R.color.colorBlackTP50))

        val mainTextView = view.findViewById<TextView>(R.id.snackbar_text)
        mainTextView.isAllCaps = false
        mainTextView.textSize = 16f
        mainTextView.setTextColor(ac.resources.getColor(R.color.colorWhite))
        mSnackBar.duration = 2000
        return mSnackBar
    }

    fun getMaterial(ac: Activity): Model_Material? {
        val pref =
            ac.getSharedPreferences("PREF_DATA", Context.MODE_PRIVATE)
        val gson = Gson()
        val response = pref.getString(
            "current_material",
            ""
        )
        return gson.fromJson(response, Model_Material::class.java)
    }

    fun SetMaterial(ac: Activity, model: Model_Material?) {
        val pref =
            ac.getSharedPreferences("PREF_DATA", Context.MODE_PRIVATE)
        val editor = pref.edit()
        val gson = Gson()
        val response = gson.toJson(model)
        editor.putString("current_material", response)
        editor.apply()
    }

